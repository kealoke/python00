def how_many_times(lst, item):
    """
    Counts the occurrences of an item in a list using a more playful name.

    Args:
        lst (list): The list (or "bag") to search through.
        item (any): The item ("treasure") to count.

    Returns:
        int: The number of times the item appears in the list.
    """
    return lst.count(item)
